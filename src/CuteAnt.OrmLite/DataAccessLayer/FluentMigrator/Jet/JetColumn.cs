using System;
using System.Data;

namespace CuteAnt.OrmLite.DataAccessLayer
{
	internal class JetColumn : ColumnBase<JetTypeMap, JetQuoter>
	{
		public JetColumn()
		{
		}

		internal override String FormatType(IDataColumn column)
		{
			if (column.Identity)
			{
				// In Jet an identity column always of type COUNTER which is a integer type
				if (column.DbType != CommonDbType.Integer)
				{
					throw new ArgumentException("Jet Engine only allows identity columns on integer columns");
				}

				return "COUNTER";
			}
			return base.FormatType(column);
		}

		internal override String FormatIdentity(IDataColumn column)
		{
			//Identity type is handled by FormatType
			return String.Empty;
		}

		internal override String FormatSystemMethods(SystemMethods systemMethod)
		{
			// 苦竹修改，去除异常，返回空值
			//throw new NotImplementedException();
			return null;
		}
	}
}